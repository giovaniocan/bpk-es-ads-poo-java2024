public class ex_02 {
    public static void main(String[] args) {
        int numeroInteiro = 42;
        double numeroDecimal = 3.14;
        boolean valorBooleano = true;
        char caractere = 'A';

        System.out.println("Valor do inteiro: " + numeroInteiro);
        System.out.println("Valor do double: " + numeroDecimal);
        System.out.println("Valor do boolean: " + valorBooleano);
        System.out.println("Valor do char: " + caractere);
    }
}
